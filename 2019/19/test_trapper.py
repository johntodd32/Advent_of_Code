import trapper


def test_fit_ship():
    field_string = '''
#.......................................
.#......................................
..##....................................
...###..................................
....###.................................
.....####...............................
......#####.............................
......######............................
.......#######..........................
........########........................
.........#########......................
..........#########.....................
...........##########...................
...........############.................
............############................
.............#############..............
..............##############............
...............###############..........
................###############.........
................#################.......
.................########OOOOOOOOOO.....
..................#######OOOOOOOOOO#....
...................######OOOOOOOOOO###..
....................#####OOOOOOOOOO#####
.....................####OOOOOOOOOO#####
.....................####OOOOOOOOOO#####
......................###OOOOOOOOOO#####
.......................##OOOOOOOOOO#####
........................#OOOOOOOOOO#####
.........................OOOOOOOOOO#####
..........................##############
..........................##############
...........................#############
............................############
.............................###########'''
    field = {}
    x = 0
    y = -1
    for c in field_string:
        if c == '\n':
            x = 0
            y = y + 1
        elif c == '#' or c == 'O':
            field[(x, y)] = 1
            x = x + 1
        else:
            x = x + 1
    assert trapper.fit_ship(field, 10) == (25, 20)
